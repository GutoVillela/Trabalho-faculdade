    private void ConsultarNomeFornecedor(){
        consultar.setRazaoSocial(txtBusca.getText());
        listaConsulta = consultar.ConsultarPorNome();

        DefaultListModel satanas = new DefaultListModel();

        for (int i = 0; i < listaConsulta.size(); i++) {

            satanas.addElement(listaConsulta.get(i).getRazaoSocial());

        }
        demonio = satanas;
        listconsulta.setModel(demonio);

        txtFornecedor.setEditable(false);
        txtCnpj.setEditable(false);
        txtCodigo.setEditable(false);
        txtEndereco.setEditable(false);
        txtN.setEditable(false);
        txtBairro.setEditable(false);
        txtCep.setEditable(false);
        txtTelefone.setEditable(false);
        txtCelular.setEditable(false);
        txtemail.setEditable(false);
    }
    
    
    
