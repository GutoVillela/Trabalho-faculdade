    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        FornecedorBLL fbll = listaConsulta.get(indice);
        fbll.setRazaoSocial(txtFornecedor.getText());
        fbll.setCnpj(txtCnpj.getText().replace(".", "").replace("-", ""));
        fbll.getEndereco().setCodigo(listaConsulta.get(indice).getEndereco().getCodigo());
        fbll.getEndereco().setLogradouro(txtEndereco.getText()); System.out.println(fbll.getEndereco().getCodigo());
        fbll.getEndereco().setNumero(txtN.getText());
        fbll.getEndereco().getBairro().setNome(txtBairro.getText());System.out.println(fbll.getEndereco().getBairro());
        fbll.getEndereco().setCep(txtCep.getText().replace(".", "").replace("-", ""));System.out.println(fbll.getEndereco().getCep());
        fbll.setEmail(txtemail.getText());
        fbll.setCodigo(Integer.parseInt(txtCodigo.getText()));
        fbll.setAtivo(true);

        if(fbll.Atualizar()){
            JOptionPane.showMessageDialog(null, "Cadastro do cliente alterado com sucesso", "Alteração efetuada com sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "NÃO DEU CERTO :(", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnsalvarActionPerformed

    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
        FornecedorBLL fbll = new FornecedorBLL();
        fbll.setCodigo(Integer.parseInt(txtCodigo.getText()));
        
        if(fbll.Desativar()){
            JOptionPane.showMessageDialog(null, "Fornecedor excluido com sucesso", "Exclusão efetuada com sucesso", JOptionPane.INFORMATION_MESSAGE);
            
            DefaultListModel satanas = (DefaultListModel) demonio;
            satanas.remove(listconsulta.getSelectedIndex());
            demonio=satanas;
            listconsulta.setModel(demonio);
            
            Limpar();
        
        } else {
            JOptionPane.showMessageDialog(null, "NÃO DEU CERTO :(", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnexcluirActionPerformed

