    private void btnCadCargoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadCargoActionPerformed
        // TODO add your handling code here:

        //DEFINIR ATRIBUTOS DO CARGO
        cBLL.setCargo(txtNomeCargo.getText());
        cBLL.setSalario(Float.valueOf(txtSalario.getText()));
        cBLL.setPermissao(listaPermissoes.get(cmbPermissoes.getSelectedIndex()));

        //CADASTRAR CARGO
        if (cBLL.Cadastrar()) {
            int escolha = JOptionPane.showConfirmDialog(null, "Cargo cadastrado com sucesso.\nDeseja cadastrar outro cargo?", "CADASTRO REALIZADO COM SUCESSO", JOptionPane.YES_NO_OPTION);

            //CASO O USUÁRIO ESCOLHA NÃO
            if (escolha == JOptionPane.NO_OPTION) {
                this.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Aconteceu um erro ao cadastrar o cargo.", "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnCadCargoActionPerformed

